type TranslationKey = keyof typeof messages["fr"];

const getBrowserLocale = (): Locale => {
  if (typeof navigator === "undefined") return defaultLocale;
  const lang = navigator.language.toLowerCase();
  if (lang.startsWith("en")) return "en";
  return "fr";
};

const formatTemplate = (template: string, vars?: Record<string, string | number>) => {
  if (!vars) return template;
  return Object.entries(vars).reduce((acc, [key, value]) => acc.replace(new RegExp(`\\{${key}\\}`, "g"), String(value)), template);
};

export const translate = (locale: Locale, key: TranslationKey, vars?: Record<string, string | number>) => {
  const message = messages[locale][key] ?? messages[defaultLocale][key] ?? key;
  return formatTemplate(message, vars);
};

const localeFromStorage = (): Locale => {
  if (typeof window === "undefined") return defaultLocale;
  const value = window.localStorage.getItem(STORAGE_KEY);
  if (value === "fr" || value === "en") return value;
  return getBrowserLocale();
};

const LocaleContext = createContext({ locale: defaultLocale, setLocale: (locale: Locale) => {} });

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(defaultLocale);

  useEffect(() => {
    setLocaleState(localeFromStorage());
  }, []);

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, locale);
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const contextValue = useMemo(
    () => ({
      locale,
      setLocale: (nextLocale: Locale) => {
        if (nextLocale !== locale) {
          setLocaleState(nextLocale);
        }
      },
    }),
    [locale],
  );

  return createElement(LocaleContext.Provider, { value: contextValue }, children);
}

export function useLocale() {
  return useContext(LocaleContext);
}

export function useTranslation() {
  const { locale } = useLocale();
  return (key: TranslationKey, vars?: Record<string, string | number>) => translate(locale, key, vars);
}

export const localeLabel = (locale: Locale) => (locale === "fr" ? "FR" : "EN");
